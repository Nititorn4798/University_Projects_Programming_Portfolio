import turtle

t = turtle.Turtle()
t.speed(0)

# วาดวงกลมใบหน้า
t.goto(0, -150)
t.begin_fill()
t.color('black')
t.circle(150)
t.end_fill()

# วาดวงกลมหูซ้าย
t.goto(-120, 100)
t.begin_fill()
t.color('black')
